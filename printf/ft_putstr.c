#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	if (!s)
	{
		x += write(1, "(null)", 6);
		return (x);
	}
	while (s[i])
	{
		write(1, &s[i++], 1);
		x++;
	}
	return (x);
}
